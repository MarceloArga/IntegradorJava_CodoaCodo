    // METODO LISTAR:
    void listar(){
        String sql="select * from usuarios";
        try {
            cn=con.getConnection();
            st=cn.createStatement();
            rs=st.executeQuery(sql);
            Object[]usuarios=new Object[4];
            modelo=(DefaultTableModel)TablaDatos.getModel();
            while (rs.next()){
            usuarios[0]=rs.getInt("Id");
            usuarios[1]=rs.getString("Nombre");
            usuarios[2]=rs.getString("Apellido");
            usuarios[3]=rs.getString("Direccion");
            modelo.addRow(usuarios);
            }
            TablaDatos.setModel(modelo);
        } catch (Exception e) {
        } finally {
        }
    }
    // METODO AGREGAR:
    void Agregar(){
        String nom=txtNom.getText();
        String ape=txtApe.getText();
        String dir=txtDir.getText();
        
        if(nom.equals("")|| ape.equals("") || dir.equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese los Datos");
        }else{
            String sql="Insert into usuarios(Nombre,Apellido,Direccion)values('"+nom+"','"+ape+"','"+dir+"')";
            try {
                cn=con.getConnection();
                st=cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Usuario Ingresado");
                limpiartabla();
            } catch (Exception e) {
            }
        }
    }
    // METODO LIMPIAR TABLA:
    void limpiartabla(){
        for(int i=0;i<=TablaDatos.getRowCount();i++){
            modelo.removeRow(i);
            i=i-1;
        }
        
    }
