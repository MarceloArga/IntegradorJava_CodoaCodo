       public void CrearXML() throws IOException, SQLException {
            
              String filePath = "D:/Web-Fundaci√≥n/IntegradorJava_CodoaCodo/IntegradorJava/voluntarios.xml";
              
               Path path = Paths.get(filePath);
               Files.delete(path);
               
               try{                             //CONEXION A BASE DE DATOS
                   String sql="Selec * from usuarios";
                    cn=con.getConnection();
                    st=cn.createStatement();
                    rs=st.executeQuery(sql);
                    
		} catch (SQLException e1) {
			e1.printStackTrace();
                        
                }
               
               String line ="<?xml version=\"1.0\" encoding=\"utf-8\"?>";  //Coloco la cabecera
                FileWriter cb = new FileWriter(filePath, true);
                cb.write(line);
                cb.close();
     
                line = "<usuarios>"; //abro el XML
                FileWriter ap = new FileWriter(filePath, true);
                ap.write(line);
                ap.close();
             
            while (rs.next()) {
          
            line = "<voluntario><nombre>" 
                    + rs.getString("Nombre") + "</nombre><apellido>" 
                    + rs.getString("Apellido") + "</apellido><direccion>" 
                    + rs.getString("Direccion") + "</direccion></voluntario>";
            
            FileWriter fw = new FileWriter(filePath, true);
            fw.write(line);
            fw.close();
            }
         
            line = "</usuarios>";
     FileWriter fo = new FileWriter(filePath, true);
     fo.write(line);
     fo.close();
         }
    
